def calculate_send_date_bg(start_date: datetime, delay_days: int) -> datetime:
    """Изчисли кога да изпратиш имейл базиран на начална дата и забавяне"""
    return start_date + timedelta(days=delay_days)

def get_next_monday_bg(date: datetime) -> datetime:
    """Получи следващия понеделник от дадена дата (за понеделнишни сутрешни имейли)"""
    days_ahead = 0 - date.weekday()  # Понеделник е 0
    if days_ahead <= 0:  # Целевият ден вече се е случил тази седмица
        days_ahead += 7
    return date + timedelta(days_ahead)